#' Multiplica Dois Numeros
#'
#' \code{multiplicar()} retorna o produto entre \code{x} e \code{y}.
#'
#' @param x Um numero
#' @param y Um numero
#' @return A produto entre \code{x} e \code{y}
#' @examples
#' # Multiplica 5 * 3 = 15
#' multiplicar(5, 3)
#' @export
multiplicar <- function(x, y) {
  x * y
}
